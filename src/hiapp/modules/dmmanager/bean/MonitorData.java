package hiapp.modules.dmmanager.bean;

public class MonitorData {
	private String importId;
	private Integer totalNum;
	private Integer sourceNum;
	private Integer midNum;
	private Integer zxNum;
	public String getImportId() {
		return importId;
	}
	public void setImportId(String importId) {
		this.importId = importId;
	}
	public Integer getTotalNum() {
		return totalNum;
	}
	public void setTotalNum(Integer totalNum) {
		this.totalNum = totalNum;
	}
	public Integer getSourceNum() {
		return sourceNum;
	}
	public void setSourceNum(Integer sourceNum) {
		this.sourceNum = sourceNum;
	}
	public Integer getMidNum() {
		return midNum;
	}
	public void setMidNum(Integer midNum) {
		this.midNum = midNum;
	}
	public Integer getZxNum() {
		return zxNum;
	}
	public void setZxNum(Integer zxNum) {
		this.zxNum = zxNum;
	}
	
}
