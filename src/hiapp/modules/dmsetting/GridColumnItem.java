package hiapp.modules.dmsetting;

public class GridColumnItem {
	private String title;
	private String width;
	private String WorkSheetColumnName;
	private String operation;
	private String columnName;
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getWidth() {
		return width;
	}
	public void setWidth(String width) {
		this.width = width;
	}
	public String getWorkSheetColumnName() {
		return WorkSheetColumnName;
	}
	public void setWorkSheetColumnName(String workSheetColumnName) {
		WorkSheetColumnName = workSheetColumnName;
	}
	public String getOperation() {
		return operation;
	}
	public void setOperation(String operation) {
		this.operation = operation;
	}
	public String getColumnName() {
		return columnName;
	}
	public void setColumnName(String columnName) {
		this.columnName = columnName;
	}
}
